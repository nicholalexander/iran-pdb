/**
 * Iran PDB Shared Navigation & Footer Injection
 * Automatically injects consistent nav and footer into all pages
 */

(function() {
  'use strict';
  
  // Detect current page path for navigation highlighting
  const currentPath = window.location.pathname;
  const isHomePage = currentPath === '/' || currentPath.endsWith('/index.html') || currentPath.endsWith('/iran-briefings/');
  const isBriefingPage = currentPath.includes('/briefings/');
  
  // Determine base path for navigation links
  let basePath = '';
  if (currentPath.includes('/briefings/')) {
    basePath = '../';
  } else if (currentPath.includes('/analysis/') || currentPath.includes('/historical/')) {
    basePath = '../';
  } else {
    basePath = '';
  }
  
  // Navigation HTML
  const navigationHTML = `
    <nav>
      <div class="container">
        <ul>
          <li><a href="${basePath}index.html" class="${isHomePage ? 'nav-home' : ''}">🏠 Home</a></li>
          <li><a href="${basePath}briefings/" class="${isBriefingPage ? 'nav-active' : ''}">📋 Latest Briefing</a></li>
          <li><a href="${basePath}feeds/">📡 Feed</a></li>
          <li><a href="${basePath}map/">🗺️ Map</a></li>
          <li><a href="${basePath}timeline/">📅 Timeline</a></li>
          <li><a href="${basePath}sources/">📰 Sources</a></li>
          <li><a href="${basePath}analysis/">📊 Analysis</a></li>
          <li><a href="${basePath}data/">💾 Data</a></li>
        </ul>
      </div>
    </nav>
  `;
  
  // Footer HTML (standard AI-generated disclosure)
  const footerHTML = `
    <footer class="container">
      <span class="warn">⚠️ SOURCE &amp; METHODOLOGY DISCLOSURE</span>
      This briefing is generated by an AI system (Claude/Anthropic) operating autonomously. It is NOT produced by human analysts, journalists, or intelligence professionals. Sources are drawn from publicly available reporting across 15+ languages, including wire services, state media, regional outlets, and social media. All claims are attributed and rated by confidence level (CONFIRMED / REPORTED / CLAIMED / UNCONFIRMED), but these ratings reflect the AI's assessment and may contain errors. This product should NOT be treated as verified intelligence, professional analysis, or a substitute for expert judgment. It is an automated aggregation and synthesis tool. Use at your own discretion. No classified or restricted sources are used. All source material is open-source.
    </footer>
  `;
  
  // Function to inject navigation after header
  function injectNavigation() {
    const header = document.querySelector('header');
    if (header && !document.querySelector('nav')) {
      header.insertAdjacentHTML('afterend', navigationHTML);
    }
  }
  
  // Function to inject footer at the end of body
  function injectFooter() {
    if (!document.querySelector('footer')) {
      document.body.insertAdjacentHTML('beforeend', footerHTML);
    }
  }
  
  // Function to get the latest briefing for homepage
  function getLatestBriefing() {
    // This will be populated by the briefing pipeline
    // For now, return a placeholder that matches the current pattern
    const briefingEntries = document.querySelectorAll('.briefing-list li');
    if (briefingEntries.length > 0) {
      const latest = briefingEntries[0];
      const dateElement = latest.querySelector('.briefing-date');
      const titleElement = latest.querySelector('.briefing-title a');
      
      if (dateElement && titleElement) {
        return {
          date: dateElement.textContent.trim(),
          title: titleElement.textContent.trim(),
          url: titleElement.getAttribute('href')
        };
      }
    }
    
    // Fallback
    return {
      date: '2026-03-07 13:00',
      title: 'Latest Iran Intelligence Brief',
      url: 'briefings/2026-03-07-0800.html'
    };
  }
  
  // Function to create hero briefing section for homepage
  function createHeroBriefing() {
    if (isHomePage) {
      const main = document.querySelector('main');
      const classification = document.querySelector('.classification');
      
      if (main && classification) {
        const latest = getLatestBriefing();
        const heroHTML = `
          <div class="hero-briefing">
            <h2>📋 Latest Intelligence Brief</h2>
            <span class="briefing-date monospace">${latest.date}</span>
            <a href="${latest.url}" class="briefing-link">${latest.title}</a>
          </div>
        `;
        classification.insertAdjacentHTML('afterend', heroHTML);
      }
    }
  }
  
  // Initialize when DOM is ready
  function initialize() {
    injectNavigation();
    injectFooter();
    createHeroBriefing();
  }
  
  // Run initialization
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initialize);
  } else {
    initialize();
  }
  
})();