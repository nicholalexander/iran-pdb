/**
 * Iran PDB Shared Navigation & Footer Injection
 * Hamburger menu on mobile, inline nav on desktop
 */

(function() {
  'use strict';
  
  const currentPath = window.location.pathname;
  const isHomePage = currentPath === '/' || currentPath.endsWith('/index.html') || currentPath.endsWith('/iran-briefings/') || currentPath.endsWith('/iran-pdb/');
  const isBriefingPage = currentPath.includes('/briefings/');
  
  // Determine base path
  let basePath = '';
  if (currentPath.includes('/briefings/') || currentPath.includes('/analysis/') || currentPath.includes('/historical/')) {
    basePath = '../';
  }
  
  const links = [
    { href: 'index.html', label: '🏠 Home', active: isHomePage },
    { href: 'briefings/', label: '📋 Briefings', active: isBriefingPage },
    { href: 'feeds/', label: '📡 Feed' },
    { href: 'map/', label: '🗺️ Map' },
    { href: 'timeline/', label: '📅 Timeline' },
    { href: 'sources/', label: '📰 Sources' },
    { href: 'analysis/', label: '📊 Analysis' },
    { href: 'data/', label: '💾 Data' },
  ];

  const navItems = links.map(l => {
    const cls = l.active ? ' class="active"' : '';
    return `<li><a href="${basePath}${l.href}"${cls}>${l.label}</a></li>`;
  }).join('\n          ');

  const navigationHTML = `
    <nav class="pdb-nav" role="navigation" aria-label="Main navigation">
      <div class="container">
        <button class="hamburger" aria-label="Toggle menu" aria-expanded="false" onclick="this.setAttribute('aria-expanded', this.getAttribute('aria-expanded')==='false'?'true':'false'); this.closest('.pdb-nav').classList.toggle('open');">
          <span></span><span></span><span></span>
        </button>
        <ul>
          ${navItems}
        </ul>
      </div>
    </nav>
  `;

  const footerHTML = `
    <footer class="container">
      <span class="warn">⚠️ SOURCE &amp; METHODOLOGY DISCLOSURE</span>
      This briefing is generated by an AI system (Claude/Anthropic) operating autonomously. It is NOT produced by human analysts, journalists, or intelligence professionals. Sources are drawn from publicly available reporting across 15+ languages, including wire services, state media, regional outlets, and social media. All claims are attributed and rated by confidence level (CONFIRMED / REPORTED / CLAIMED / UNCONFIRMED), but these ratings reflect the AI's assessment and may contain errors. This product should NOT be treated as verified intelligence, professional analysis, or a substitute for expert judgment. It is an automated aggregation and synthesis tool. Use at your own discretion. No classified or restricted sources are used. All source material is open-source.
    </footer>
  `;

  function initialize() {
    const header = document.querySelector('header');
    if (header && !document.querySelector('.pdb-nav')) {
      header.insertAdjacentHTML('afterend', navigationHTML);
    }
    if (!document.querySelector('footer')) {
      document.body.insertAdjacentHTML('beforeend', footerHTML);
    }
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initialize);
  } else {
    initialize();
  }
})();
